/**
 * Get the translation for a specific key
 * 
 * @param {string} key - The translation key
 * @param {string} language - The language to use (defaults to english)
 * @returns {string} The translated text
 */
export function translate(key, language = 'english') {
  // Default to english if the requested language doesn't exist
  const languageSet = translations[language] || translations.english;
  
  // Return the translation or the key itself if not found
  return languageSet[key] || key;
}

/**
 * Get all translations for the current language
 * 
 * @param {string} language - The language to use (defaults to english)
 * @returns {Object} All translations for the language
 */
export function getTranslations(language = 'english') {
  return translations[language] || translations.english;
} 
 